package com.cisco.scm.view;

public class ReviewComponentSwapData {

	private String pidId;
	private String swappedComponent;
	private String changedQuantity;
	
	public String getPidId() {
		return pidId;
	}
	public void setPidId(String pidId) {
		this.pidId = pidId;
	}
	public String getSwappedComponent() {
		return swappedComponent;
	}
	public void setSwappedComponent(String swappedComponent) {
		this.swappedComponent = swappedComponent;
	}
	public String getChangedQuantity() {
		return changedQuantity;
	}
	public void setChangedQuantity(String changedQuantity) {
		this.changedQuantity = changedQuantity;
	}
	
	
}
